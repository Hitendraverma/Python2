#!/usr/bin/python
import math

print("math.ceil for next higher number")
print(math.ceil(34.98))
print("math.sqrt for square root of a number")
print(math.sqrt(25))
print("math.floor for next lower number")
print(math.floor(34.90))
print("math.e is exponential")
print(math.exp(7))
print("math.log of 5" , math.log(5))
print("math.log10(2)" , math.log10(2))
print("math.log(math.e)", math.log(math.e))

